    private void btnWriteFileNhiPhanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnWriteFileNhiPhanActionPerformed
// 1. Khởi tạo mảng byte để lưu ký tự. Kích thước 20 là kích thước cố định, cần kiểm tra lại
    byte b[] = new byte[20]; 
    File file;
    int i;
    
    // 2. Chuyển nội dung từ txtNhapChuoi sang mảng ký tự và sau đó sang mảng byte b[]
    char s[] = txtNhapChuoi.getText().toCharArray(); 
    for (i = 0; i < s.length; i++) {
        b[i] = (byte) s[i];
    }
    
    // 3. Xử lý ghi file
    try {
        // Tạo đối tượng File từ tên file nhập vào
        file = new File(txtFileName.getText());
        
        // Tạo luồng ghi file nhị phân
        FileOutputStream fo = new FileOutputStream(file); 
        
        // Ghi mảng byte b[] vào file (ghi 20 bytes cố định, bao gồm cả những byte trống)
        fo.write(b); 
        
        // Đóng luồng
        fo.close(); 
        
    } catch(IOException e) { 
        e.printStackTrace(); 
    }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnWriteFileNhiPhanActionPerformed

    private void btnReadFileNhiPhanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReadFileNhiPhanActionPerformed
byte b[] = new byte[20]; 
    File file;
    
    try {
        file = new File(txtFileName.getText());
        FileInputStream fi = new FileInputStream(file);
        
        // Kiểm tra xem file có sẵn dữ liệu để đọc không
        if (fi.available() > 0) {
            // Đọc dữ liệu vào mảng b
            fi.read(b); 
        }
        fi.close();
        
        // Đọc và hiển thị nội dung
        String s = new String(b);
        txtHienThiKetQua.setText(s.trim()); // trim() để loại bỏ ký tự trắng thừa nếu mảng b lớn hơn dữ liệu
        
    } catch(Exception e) {
        JOptionPane.showMessageDialog(this, e.toString());
    }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnReadFileNhiPhanActionPerformed

