    private void btnReadFileVanBanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReadFileVanBanActionPerformed
try {
        // Khởi tạo FileReader để đọc file
        FileReader fr = new FileReader(new File(txtFileName.getText()));
        
        // Khởi tạo BufferedReader để đọc hiệu quả hơn
        BufferedReader br = new BufferedReader(fr);
        
        // Khởi tạo StringBuffer để xây dựng chuỗi kết quả
        StringBuffer sb = new StringBuffer(); 
        
        // Mảng char để đọc từng khối dữ liệu (đọc tối đa 512 ký tự mỗi lần)
        char ca[] = new char[512]; 
        int i;
        
        // Lặp cho đến khi không còn dữ liệu để đọc (br.read trả về -1)
        while ((i = br.read(ca)) != -1) { 
            // Thêm dữ liệu đã đọc vào StringBuffer
            sb.append(String.valueOf(ca, 0, i)); 
        }
        
        br.close();
        fr.close();
        
        // Hiển thị kết quả đọc được vào ô hiển thị kết quả
        txtHienThiKetQua.setText(sb.toString()); 
        
    } catch(Exception e) {
        e.printStackTrace();
    }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnReadFileVanBanActionPerformed

