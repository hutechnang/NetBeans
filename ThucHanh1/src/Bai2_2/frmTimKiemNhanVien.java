    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        Vector<Vector<String>> data = null;
        
        if ("NGACH".equals(cheDoLietKe)) {
            String ngach = (String) cbNgach.getSelectedItem();
            data = QLNhanVien.quanLy.lietKeTheoNgach(ngach);
        } else if ("PHONGBAN".equals(cheDoLietKe)) {
            String phongBan = txtPhongBan.getText().trim();
            if (phongBan.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập phòng ban!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
                return;
            }
            data = QLNhanVien.quanLy.lietKeTheoPhongBan(phongBan);
        } else if ("LOAIHD".equals(cheDoLietKe)) {
            String loaiHD = (String) cbLoaiHD.getSelectedItem();
            data = QLNhanVien.quanLy.lietKeTheoLoaiHopDong(loaiHD);
        }
        
        if (data != null) {
            loadDataToTable(data);
            if (data.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Không tìm thấy nhân viên nào!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnTimKiemActionPerformed

