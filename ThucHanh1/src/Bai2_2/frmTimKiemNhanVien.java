    public void setCheDoLietKe(String cheDo) {
        this.cheDoLietKe = cheDo;
        if ("NGACH".equals(cheDo)) {
            lblTieuDe.setText("Liệt kê theo ngạch");
            pnlTimKiem.setVisible(true);
            cbNgach.setVisible(true);
            txtPhongBan.setVisible(false);
            cbLoaiHD.setVisible(false);
        } else if ("PHONGBAN".equals(cheDo)) {
            lblTieuDe.setText("Liệt kê theo phòng ban");
            pnlTimKiem.setVisible(true);
            cbNgach.setVisible(false);
            txtPhongBan.setVisible(true);
            cbLoaiHD.setVisible(false);
        } else if ("LOAIHD".equals(cheDo)) {
            lblTieuDe.setText("Liệt kê theo loại hợp đồng");
            pnlTimKiem.setVisible(true);
            cbNgach.setVisible(false);
            txtPhongBan.setVisible(false);
            cbLoaiHD.setVisible(true);
        }
    }
    
    public void hienThiTatCa() {
        Vector<Vector<String>> data = QLNhanVien.quanLy.hienThiTatCa();
        loadDataToTable(data);
        lblTieuDe.setText("Danh sách tất cả nhân viên");
        pnlTimKiem.setVisible(false);
    }
    
    private void loadDataToTable(Vector<Vector<String>> data) {
        Vector<String> columnNames = new Vector<>();
        columnNames.add("Họ tên");
        columnNames.add("Phòng ban");
        columnNames.add("Loại NV");
        columnNames.add("Lương");
        
        DefaultTableModel model = new DefaultTableModel(data, columnNames);
        tableDanhSach.setModel(model);
    }

